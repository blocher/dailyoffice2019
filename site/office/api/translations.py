def translate(text, language):
    """Translate a string based on the display_language setting."""
    if language in ("chinese-traditional", "chinese-simplified"):
        return CHINESE.get(text, text)
    if language == "spanish":
        return SPANISH.get(text, text)
    return text


def is_chinese(language):
    """Check if the language is any Chinese variant."""
    return language in ("chinese-traditional", "chinese-simplified")


def is_spanish(language):
    """Check if the language is Spanish."""
    return language == "spanish"


def get_csv_suffix(language):
    """Return the CSV filename suffix for non-English languages."""
    if is_chinese(language):
        return "_chinese"
    if is_spanish(language):
        return "_spanish"
    return ""


# Keep backward compatibility
def get_chinese_csv_suffix(language):
    """Deprecated: use get_csv_suffix instead."""
    return get_csv_suffix(language)
