class Command(BaseCommand):
    help = "Import Chinese collect translations from the Anglican Church of Canada BAS"

    def add_arguments(self, parser):
        parser.add_argument("--dry-run", action="store_true", help="Show what would be updated without saving")

    def handle(self, *args, **options):
        dry_run = options["dry_run"]
        updated = 0
        not_found = 0

        for title, chinese_text in CHINESE_COLLECTS.items():
            try:
                collect = Collect.objects.get(title=title)
                if dry_run:
                    self.stdout.write(f"  Would update: {title}")
                else:
                    collect.chinese_text = chinese_text
                    collect.save()
                    self.stdout.write(f"  Updated: {title}")
                updated += 1
            except Collect.DoesNotExist:
                self.stdout.write(f"  NOT FOUND: {title}")
                not_found += 1
            except Collect.MultipleObjectsReturned:
                collect = Collect.objects.filter(title=title).first()
                if not dry_run:
                    collect.chinese_text = chinese_text
                    collect.save()
                self.stdout.write(f"  Updated (first match): {title}")
                updated += 1

        self.stdout.write(f"\nTotal updated: {updated}, Not found: {not_found}")
        if dry_run:
            self.stdout.write("(Dry run - no changes saved)")
